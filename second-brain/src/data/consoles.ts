// Get console data by platform name
export function getConsoleData(platformName: string): ConsoleData | null {
  const lower = platformName.toLowerCase().trim();
  return CONSOLES.find(c =>
    c.names.some(n => n.toLowerCase() === lower)
  ) || null;
}

// Get 3 daily rotating facts (seeded by day of year so they change daily)
export function getDailyFacts(facts: string[]): string[] {
  const now = new Date();
  const dayOfYear = Math.floor(
    (now.getTime() - new Date(now.getFullYear(), 0, 0).getTime()) / 86400000
  );
  const rotationIndex = dayOfYear % Math.max(facts.length - 2, 1);
  const selected: string[] = [];
  for (let i = 0; i < 3; i++) {
    selected.push(facts[(rotationIndex + i) % facts.length]);
  }
  return selected;
}
